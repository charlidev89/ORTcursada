package Asadito_SimilParcial;

public class Amigo {
	private String nombre;
	private String domicilio;
	
	
	public Amigo(String nombre, String domicilio) {
		super();
		this.nombre = nombre;
		this.domicilio = domicilio;
	}


	public String getNombre() {
		return nombre;
	}


	public String getDomicilio() {
		return domicilio;
	}
    
}
