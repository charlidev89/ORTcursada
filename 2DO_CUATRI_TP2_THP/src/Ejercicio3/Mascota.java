package Ejercicio3;

public class Mascota {
	private String nombre;
	private String tipoAnimal;

	public Mascota(String nombre, String tipoAnimal) {
		
		this.nombre = nombre;
		this.tipoAnimal = tipoAnimal;
	}

	public String getNombre() {
		return nombre;
	}

	public String getTipoAnimal() {
		return tipoAnimal;
	}

}
